// Parser holds curToken and peekToken like: position and readPosition
type Parser struct {
	l         *lexer.Lexer
	curToken  token.Token
	peekToken token.Token
	errors    []string
}

func NewParse(l *lexer.Lexer) *Parser {
	p := &Parser{
		l: l,
	}
	// Read two tokens in order to that curToken and peekToken are both set
	p.nextToken()
	p.nextToken()
	return p
}

func (p *Parser) nextToken() {
	p.curToken = p.peekToken
	p.peekToken = p.l.NextToken()
}

